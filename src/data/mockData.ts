// Helper functions
export const getPropertyById = (id: string): Property | undefined => {
  return mockProperties.find(property => property.id === id);
};

export const getPropertiesByStayType = (stayType: StayType): Property[] => {
  return mockProperties.filter(property => property.stayType === stayType);
};

export const getAvailableProperties = (): Property[] => {
  return mockProperties.filter(property => property.isAvailable);
};

export const searchProperties = (filters: {
  location?: string;
  stayType?: StayType[];
  maxPrice?: number;
  minPrice?: number;
}): Property[] => {
  return mockProperties.filter(property => {
    if (filters.location && !property.city.toLowerCase().includes(filters.location.toLowerCase())) {
      return false;
    }
    if (filters.stayType && !filters.stayType.includes(property.stayType)) {
      return false;
    }
    if (filters.maxPrice && property.basePrice > filters.maxPrice) {
      return false;
    }
    if (filters.minPrice && property.basePrice < filters.minPrice) {
      return false;
    }
    return true;
  });
};

